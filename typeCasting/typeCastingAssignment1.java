package typeCasting;

public class typeCastingAssignment1 {

	public static void main(String[] args) {
		
		// converting int -> float , double , long
		
		int Amount = 3000;
		
		float fAmount = Amount;
		double dAmount = Amount;
		long lAmount = Amount;
		
		System.out.println(fAmount+ "  " + dAmount + "  " + lAmount);
		System.out.println("---------------------------------------");
		
		//converting byte -> float , double , long
		
		byte number = 20;
		
		float fNumber = number;
		double dNumber = number;
		long lNumber = number;
		
		System.out.println(fNumber + "  " + dNumber + "  " + lNumber);
		System.out.println("---------------------------------------");
		
		//converting short -> float , double , long
		
		short value = 274;
		
		
		float fValue = value;
		double dValue = value;
		long lValue = value;
		
		System.out.println(fValue + "  " + dValue + "  " + lValue);
		System.out.println("---------------------------------------");
		

	}

}
